package com.designhub.fitnessplus.bean;

public class GymImageBean {
	
	private String gymImagePath;
	private String gymDetailsId;
	private String gymImageId;
	
	public String getGymImagePath() {
		return gymImagePath;
	}

	public void setGymImagePath(String gymImagePath) {
		this.gymImagePath = gymImagePath;
	}


	public String getGymDetailsId() {
		return gymDetailsId;
	}

	public void setGymDetailsId(String gymDetailsId) {
		this.gymDetailsId = gymDetailsId;
	}

	public String getGymImageId() {
		return gymImageId;
	}

	public void setGymImageId(String gymImageId) {
		this.gymImageId = gymImageId;
	}

	
	

}
